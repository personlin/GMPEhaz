         term5 = c11(1)*alog(vs/k1(1)) +
     1           k2(1)*(alog(pgarock+c*((vs/k1(1))**n)) -
     2           alog(pgarock+c))
      else
         term5 = (c11(1) + k2(1)*n)*alog(vs/k1(1))
